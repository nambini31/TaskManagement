// Validation & Phone mask
(function () {
  const phoneMaskList = document.querySelectorAll('.phone-mask'),
    addNewUserForm = document.getElementById('addNewUserForm');

  // Phone Number
  if (phoneMaskList) {
    phoneMaskList.forEach(function (phoneMask) {
      new Cleave(phoneMask, {
        phone: true,
        phoneRegionCode: 'US'
      });
    });
  }
  // Add New User Form Validation
  const fv = FormValidation.formValidation(addNewUserForm, {
    fields: {
        name: {
            validators: {
                notEmpty: {
                message: 'Please enter Name '
                }
            }
        },
        surname: {
            validators: {
                notEmpty: {
                    message: 'Please enter Surname '
                }
            }
        },
        email: {
            validators: {
                notEmpty: {
                message: 'Please enter your email'
                },
                emailAddress: {
                message: 'The value is not a valid email address'
                }
            }
          },
          username: {
              validators: {
                  notEmpty: {
                      message: 'Please enter your username'
                  }
              }
          },
          password: {
              validators: {
                  notEmpty: {
                      message: 'Please enter a password'
                  },
                  stringLength: {
                      min: 6,
                      message: 'Password must be at least 6 characters long'
                  }
              }
          },
          confirmPassword: {
              validators: {
                  notEmpty: {
                      message: 'Please confirm your password'
                  },
                  identical: {
                      compare: function () {
                          return addNewUserForm.querySelector('[name="password"]').value;
                      },
                      message: 'The password and its confirm are not the same'
                  },
              }
          }

    },
    plugins: {
      trigger: new FormValidation.plugins.Trigger(),
      bootstrap5: new FormValidation.plugins.Bootstrap5({
        // Use this for enabling/changing valid/invalid class
        eleValidClass: '',
        rowSelector: function (field, ele) {
          // field is the field name & ele is the field element
          return '.mb-3';
        }
      }),
      submitButton: new FormValidation.plugins.SubmitButton(),
      // Submit the form when all fields are valid
      // defaultSubmit: new FormValidation.plugins.DefaultSubmit(),
      autoFocus: new FormValidation.plugins.AutoFocus()
    }
  });

    //------- Ajout à la base de sonnée -----
    
    $('#addNewUserForm').on('submit', function (e) {
        e.preventDefault();

        alert("biby");

        fv.validate().then(function (status) {
            if (status === 'Valid') {
                $.ajax({
                    url: '/Account/Register', // Change this URL to your actual endpoint
                    type: 'POST',
                    data: $(addNewUserForm).serialize(),
                    success: function (response) {
                        // Handle success response
                        alert('User added successfully!');
                        // Optionally, you can reload the table or reset the form here
                        addNewUserForm.reset();
                        fv.resetForm(true);
                    },
                    error: function (xhr, status, error) {
                        // Handle error response
                        alert('An error occurred while adding the user: ' + error);
                    }
                });
            }
        });
    });

})();


